class _FakeMemoryService:
    """Recall-only fake; write is never invoked by planner stage eval."""

    def __init__(self, memory_context: dict[str, Any] | None) -> None:
        """Cache the fixture memory context for every recall call."""
        self._memory_context = memory_context

    async def recall(self, topic: str, *, user_id: str | None = None) -> dict[str, Any]:
        """Return the fixture memory context with the canonical empty shape."""
        del topic, user_id
        if self._memory_context is None:
            return {
                "preferences": {},
                "allow_domains": [],
                "deny_domains": [],
                "past_reports": [],
                "semantic_preferences": [],
            }
        return self._memory_context


@contextmanager
def _stub_memory_recall(memory_context: dict[str, Any] | None) -> Iterator[None]:
    """Replace the cached memory-service singleton with a fixture-backed fake.

    Planner nodes resolve memory via ``app.memory.get_memory_service()`` which
    in turn imports ``app.memory.service.get_memory_service``. We swap the
    `service`-module function so both call sites pick up the fake, and restore
    it on exit. ``lru_cache`` would otherwise pin the real singleton across
    test cases.
    """
    fake = _FakeMemoryService(memory_context)
    original_fn = memory_service_module.get_memory_service
    memory_service_module.get_memory_service = lambda: fake  # type: ignore[assignment]
    try:
        yield
    finally:
        memory_service_module.get_memory_service = original_fn  # type: ignore[assignment]


class _MockPlannerRouter:
    """Deterministic router stub used in --mock-llm mode for planner eval.

    Produces a plan of length equal to the depth profile's max so deterministic
    band checks pass without invoking any real LLM. Other task tags are
    forwarded to the wrapped router if any, otherwise raise.
    """

    def __init__(self, *, depth: str) -> None:
        """Cache the depth so we can size the synthetic plan correctly."""
        self._depth = depth

    async def structured(self, task: TaskTag, messages: list[dict[str, Any]], schema, **kwargs):
        """Return a fake PlannerOutput sized to the depth profile."""
        del messages, kwargs
        if task != "planner":
            raise AssertionError(f"_MockPlannerRouter only handles 'planner', got {task!r}")
        if schema is not PlannerOutput:
            raise AssertionError(f"_MockPlannerRouter expects PlannerOutput, got {schema!r}")
        profile = profile_for_depth(self._depth)
        n = profile.planner_max_subquestions
        return PlannerOutput(
            sub_questions=[
                SubQuestion(
                    id=f"mock-sq-{idx + 1}",
                    question=f"Mock sub-question {idx + 1} for depth {self._depth}",
                    rationale="generated by mock router",
                    suggested_tools=["web_search"],
                )
                for idx in range(n)
            ]
        )


def _deterministic_checks(
    plan: list[SubQuestion],
    *,
    min_q: int,
    max_q: int,
    max_pairwise_jaccard: float,
) -> tuple[bool, dict[str, Any], list[str]]:
    """Validate plan shape against the fixture's expected bands."""
    diagnostics: list[str] = []
    metrics: dict[str, Any] = {
        "plan_length": len(plan),
        "unique_ids": len({sq.id for sq in plan}) == len(plan),
        "min_expected": min_q,
        "max_expected": max_q,
    }

    if not plan:
        diagnostics.append("plan is empty")
    if len(plan) < min_q:
        diagnostics.append(f"plan too short: {len(plan)} < {min_q}")
    if len(plan) > max_q:
        diagnostics.append(f"plan too long: {len(plan)} > {max_q}")
    if not metrics["unique_ids"]:
        diagnostics.append("duplicate sub-question ids")

    # Pairwise near-duplicate detection on sub-question text.
    max_overlap = 0.0
    for a, b in combinations(plan, 2):
        score = jaccard(a.question, b.question)
        if score > max_overlap:
            max_overlap = score
    metrics["max_pairwise_jaccard"] = round(max_overlap, 4)
    if max_overlap > max_pairwise_jaccard:
        diagnostics.append(
            f"near-duplicate sub-questions detected (jaccard={max_overlap:.2f} "
            f"> {max_pairwise_jaccard})"
        )

    return (not diagnostics), metrics, diagnostics


async def run_planner_stage(
    *,
    mock_llm: bool = False,
    deterministic_only: bool = False,
) -> StageReport:
    """Evaluate the planner node against the planner.json fixtures.

    Args:
        mock_llm: When True, stub the planner's structured LLM call with a
            deterministic router. Useful for CI without API keys.
        deterministic_only: When True, skip the planner_judge LLM scoring call.
    """
    cases = load_stage_fixtures("planner")
    results: list[StageCaseResult] = []

    for case in cases:
        case_id = case["case_id"]
        topic = case["topic"]
        depth = normalize_depth(case.get("depth"))
        memory_context = case.get("memory_context")
        expected = case.get("expected", {})

        llm_router: Router | _MockPlannerRouter | None = (
            _MockPlannerRouter(depth=depth) if mock_llm else None
        )
        try:
            with _stub_memory_recall(memory_context):
                state = await planner_node(
                    {"topic": topic, "depth": depth},
                    llm_router=llm_router,  # type: ignore[arg-type]
                )
            plan = state.get("plan", [])
            passed, metrics, diagnostics = _deterministic_checks(
                plan,
                min_q=expected.get("min_subquestions", 1),
                max_q=expected.get("max_subquestions", 99),
                max_pairwise_jaccard=expected.get("max_pairwise_jaccard", 0.8),
            )
        except Exception as exc:
            results.append(
                StageCaseResult(
                    case_id=case_id,
                    passed=False,
                    deterministic={"error": True},
                    diagnostics=[f"planner_node raised: {exc!r}"],
                    judge=None,
                )
            )
            continue

        judge_result = None
        if not mock_llm:
            judge_result = await score_plan(
                topic=topic,
                depth=depth,
                plan=plan,
                memory_context=memory_context,
                disabled_reason=(
                    "deterministic_only enabled" if deterministic_only else None
                ),
            )

        results.append(
            StageCaseResult(
                case_id=case_id,
                passed=passed,
                deterministic=metrics,
                diagnostics=diagnostics,
                judge=judge_result,
            )
        )

    return build_stage_report(
        stage="planner",
        results=results,
        deterministic_only=deterministic_only or mock_llm,
    )
